package com.yinlian.Enums;

/**
 * 代金券状态
 * 
 * @author Administrator
 *
 */
public enum VoucherStatusEnum {
	未分配(0), 已分配(1), 已领用(2);
	private final int value;

	public int getValue() {
		return value;
	}

	// 构造器默认也只能是private, 从而保证构造函数只能在内部使用
	VoucherStatusEnum(int value) {
		this.value = value;
	}

}
