package com.yinlian.Enums;

/**
 * 评论类型
 * 
 * @author mjx
 *
 */
public enum CommentTypeEnum {
	全部(-1), 商品未评论(0), 商品评论(1), 商品晒单(2), 商品追评(3), 团购评价(4),商品晒范(5);
	private final int value;

	public int getValue() {
		return value;
	}

	// 构造器默认也只能是private, 从而保证构造函数只能在内部使用
	CommentTypeEnum(int value) {
		this.value = value;
	}
}
