package com.yinlian.Enums;

/**
 * 咨询类型
 * 
 * @author mjx
 *
 */
public enum ConsultTypeEnum {
	商品咨询(1), 库存及配送(2), 支付问题(3), 发票及保养(4), 促销及赠品(5);
	private final int value;

	public int getValue() {
		return value;
	}

	// 构造器默认也只能是private, 从而保证构造函数只能在内部使用
	ConsultTypeEnum(int value) {
		this.value = value;
	}
}
