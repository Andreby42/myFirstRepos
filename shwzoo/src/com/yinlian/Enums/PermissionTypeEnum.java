package com.yinlian.Enums;

/**
 * 权限类型
 * 
 * @author mjx
 *
 */
public enum PermissionTypeEnum {
	平台权限(1), 店铺权限(2), 卖家权限(3), 买家权限(4);
	private final int value;

	public int getValue() {
		return value;
	}

	// 构造器默认也只能是private, 从而保证构造函数只能在内部使用
	PermissionTypeEnum(int value) {
		this.value = value;
	}
}
