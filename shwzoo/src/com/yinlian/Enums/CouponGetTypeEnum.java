package com.yinlian.Enums;

/**
 * 优惠卷获取方式
 * 
 * @author mjx
 *
 */
public enum CouponGetTypeEnum {
	买家领取(0), 满返促销(1), 系统赠送(2), 生日赠送(3),抽奖专用(4),注册赠送(5);
	private final int value;

	public int getValue() {
		return value;
	}

	// 构造器默认也只能是private, 从而保证构造函数只能在内部使用
	CouponGetTypeEnum(int value) {
		this.value = value;
	}
}
