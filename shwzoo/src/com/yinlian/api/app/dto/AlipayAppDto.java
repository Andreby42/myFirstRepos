package com.yinlian.api.app.dto;

public class AlipayAppDto {

	private String appid;          //app应用ID
	private String privatekey;     //商户私钥
	private String sellerid;       //收款账号
	private String notifyurl;      //异步回调地址
	private String notifyurl_tg;      //异步回调地址  团购订单
	
	public String getAppid() {
		return appid;
	}
	public void setAppid(String appid) {
		this.appid = appid;
	}
	public String getPrivatekey() {
		return privatekey;
	}
	public void setPrivatekey(String privatekey) {
		this.privatekey = privatekey;
	}
	public String getSellerid() {
		return sellerid;
	}
	public void setSellerid(String sellerid) {
		this.sellerid = sellerid;
	}
	public String getNotifyurl() {
		return notifyurl;
	}
	public void setNotifyurl(String notifyurl) {
		this.notifyurl = notifyurl;
	}
	public String getNotifyurl_tg() {
		return notifyurl_tg;
	}
	public void setNotifyurl_tg(String notifyurl_tg) {
		this.notifyurl_tg = notifyurl_tg;
	}
	
	
}
