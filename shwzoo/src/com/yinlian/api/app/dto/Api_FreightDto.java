package com.yinlian.api.app.dto;

public class Api_FreightDto {
	
	private Integer shopId;   //店铺id
	
	private Integer firstCount;  //首件数量
	
	private Double   firstPrice;  //首件价格
	
	private Integer elseCount;   //续件数量
	
	private Double   elsePrice;   //续件价格

	public Integer getShopId() {
		return shopId;
	}

	public void setShopId(Integer shopId) {
		this.shopId = shopId;
	}

	
	public Integer getFirstCount() {
		return firstCount;
	}

	public void setFirstCount(Integer firstCount) {
		this.firstCount = firstCount;
	}

	public Integer getElseCount() {
		return elseCount;
	}

	public void setElseCount(Integer elseCount) {
		this.elseCount = elseCount;
	}

	public Double getFirstPrice() {
		return firstPrice;
	}

	public void setFirstPrice(Double firstPrice) {
		this.firstPrice = firstPrice;
	}

	public Double getElsePrice() {
		return elsePrice;
	}

	public void setElsePrice(Double elsePrice) {
		this.elsePrice = elsePrice;
	}
}
