package com.yinlian.wssc.web.dto;

import java.util.Date;

import com.yinlian.wssc.web.util.DateUtil;

/**
 * 商品销量统计
 * @author Administrator
 *
 */
public class V_saleproducts {

	private Integer shopid;            //店铺id

	private String shopname;           //店铺名称
	
    private String productnum;         //商品编号

    private String productname;        //商品名称

    private Double productprice;       //商品售价
    
    private Double totalmoney;         //合计金额

    private Integer totalcount;        //合计数量
    
    private Date orderdate;            //下单日期
    
    private Integer websites;          //下单站点
    
    private String orderdateStr;       

	public Integer getShopid() {
		return shopid;
	}

	public void setShopid(Integer shopid) {
		this.shopid = shopid;
	}

	public String getShopname() {
		return shopname;
	}

	public void setShopname(String shopname) {
		this.shopname = shopname;
	}

	public String getProductnum() {
		return productnum;
	}

	public void setProductnum(String productnum) {
		this.productnum = productnum;
	}

	public String getProductname() {
		return productname;
	}

	public void setProductname(String productname) {
		this.productname = productname;
	}

	public Double getProductprice() {
		return productprice;
	}

	public void setProductprice(Double productprice) {
		this.productprice = productprice;
	}

	public Double getTotalmoney() {
		return totalmoney;
	}

	public void setTotalmoney(Double totalmoney) {
		this.totalmoney = totalmoney;
	}

	public Integer getTotalcount() {
		return totalcount;
	}

	public void setTotalcount(Integer totalcount) {
		this.totalcount = totalcount;
	}

	public Date getOrderdate() {
		return orderdate;
	}

	public void setOrderdate(Date orderdate) {
		this.orderdate = orderdate;
	}

	public Integer getWebsites() {
		return websites;
	}

	public void setWebsites(Integer websites) {
		this.websites = websites;
	}

	public String getOrderdateStr() {
		return DateUtil.dateConvert(this.orderdate);
	}

	public void setOrderdateStr(String orderdateStr) {
		this.orderdateStr = orderdateStr;
	}
    
    
}
