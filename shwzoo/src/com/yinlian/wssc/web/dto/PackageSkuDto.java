package com.yinlian.wssc.web.dto;

public class PackageSkuDto {
	 //库存商品ID
    private Integer SkuID;
    //优惠价格
    private Float SkuPackPrice;
    //库存商品编号
    private String SkuNum;
    //库存商品名称
    private String SkuName;
    //库存商品价格
    private Float SkuPrice;
    //库存商品图片地址
    private String ImgUrl;
    
    private Integer spuid;
    
	public Integer getSkuID() {
		return SkuID;
	}
	public void setSkuID(Integer skuID) {
		SkuID = skuID;
	}
	public Float getSkuPackPrice() {
		return SkuPackPrice;
	}
	public void setSkuPackPrice(Float skuPackPrice) {
		SkuPackPrice = skuPackPrice;
	}
	public String getSkuNum() {
		return SkuNum;
	}
	public void setSkuNum(String skuNum) {
		SkuNum = skuNum;
	}
	public String getSkuName() {
		return SkuName;
	}
	public void setSkuName(String skuName) {
		SkuName = skuName;
	}
	public Float getSkuPrice() {
		return SkuPrice;
	}
	public void setSkuPrice(Float skuPrice) {
		SkuPrice = skuPrice;
	}
	public String getImgUrl() {
		return ImgUrl;
	}
	public void setImgUrl(String imgUrl) {
		ImgUrl = imgUrl;
	}
	public Integer getSpuid() {
		return spuid;
	}
	public void setSpuid(Integer spuid) {
		this.spuid = spuid;
	}
    
}
