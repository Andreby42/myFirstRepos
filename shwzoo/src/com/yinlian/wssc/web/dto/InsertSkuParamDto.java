package com.yinlian.wssc.web.dto;

import java.math.BigDecimal;

/**
 * 添加商品 库存商品信息
 * @author Administrator
 *
 */
public class InsertSkuParamDto {
	
	 private Integer      id;            //商品ID
	 
	 private String       ticketnum;     //票务商品ID
	 
	 private BigDecimal   price;         //商品价格

	 private BigDecimal   appprice;      //折扣价

	 private Integer      istoday;       //是否今日票 0-否 1-是

	 
	public String getTicketnum() {
		return ticketnum;
	}

	public void setTicketnum(String ticketnum) {
		this.ticketnum = ticketnum;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public BigDecimal getAppprice() {
		return appprice;
	}

	public void setAppprice(BigDecimal appprice) {
		this.appprice = appprice;
	}

	public Integer getIstoday() {
		return istoday;
	}

	public void setIstoday(Integer istoday) {
		this.istoday = istoday;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}
	 
	 
}
